package admin.gugan.project;

public class Loops {
	public static void main(String args[]) {
		//For loop
		for (int i=0;i<10;i++)
		{
			System.out.println(i);
		}
		//While loop
		int i=10;
		while(i<=20)
		{
			System.out.println(i);
			i++;
		}
		//Do while loop
		i=21;
		do
		{
			System.out.println(i);
			i++;
		}
		while(i<=30);

	}
	

}
